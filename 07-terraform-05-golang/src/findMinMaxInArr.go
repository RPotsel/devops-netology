package main

import "fmt"

func findMinMaxInArr(a []int) (min int, max int) {
	min = a[0]
	max = a[0]
	for _, value := range a {
		if value < min {
			min = value
		}
		if value > max {
			max = value
		}
	}
	return min, max
}

func main() {
	var x = []int{48, 96, 86, 68, 57, 82, 63, 70, 37, 34, 83, 27, 19, 97, 9, 17}
	min, max := findMinMaxInArr(x)
	fmt.Printf("Min: %v Max: %v\n", min, max)
}
